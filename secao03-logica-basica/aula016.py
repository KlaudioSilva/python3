#introdução ao If/Elif   /else

#if / elif    / else
#se / senão se  / senão
entrada = input('Você quer "Entrar" ou "Sair"? ')

if(entrada == 'Entrar'):
    print('Você entrou no sistema.')
elif(entrada == 'Sair'):
    print('Você saiu do sistema.')
else:
    print('Você não digitou "Entrar" ou "Sair".')
"""
Python = linguagem de programação
Tipo de tipagem = Dinâmica / Forte
str → string → texto
Strings são textos que estão dentro de aspas
"""

#Aspas simples
print('Klaudio Kha')

#Aspas duplas
print("DJ Klaudio Kha")

#Escape
print("\"DJ\" Klaudio \"Kha\"")

#r
print("\"DJ\" Klaudio \"Kha\"")

#método mais prático, inverter as aspas(simples dentro de duplas ou vice versa)
print("'DJ' Klaudio 'Kha'")
print('DJ "Klaudio" Kha')

print('Explicito', 'é', 'melhor " do que implicito')
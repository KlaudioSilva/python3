# Podemos fazer comentários em Python usando o símbolo de '#' (jogo da velha).
# Geralmente o '#' é usando em comentários de apenas uma linha.
# Também podemos fazer o comentário logo acima da instrução de código ou ao lado.
# Obs. Ao comentar do lado do código é importante dar 2 espaços entre o código e o comentário.

# mostre uma mensagem
print('Olá Mundo!')

print('Está é mais uma linha de código.')  # comentando do lado da instrução

"""
Para fazer comentários de várias linhas, usamos as aspas duplas.
Tudo o que estiver entre as aspas será ignorado na execução do código.
"""
print(2 + 3)

'''
Também é possível fazer comentários de várias linhas usando
apenas aspas simples.
'''
print('Eu sou um programador.')

# veja as cores
print('\033[31mvermelho\033[m')
print('\033[34mazul\033[m')
print('\033[32mverde\033[m')
